package amymialee.durabilityspeed;

public class DurabilityModConfig {
    public boolean modEnabled;
    public float maximumSpeed;
    public float minimumSpeed;
    public DurabilityModConfig(boolean a, float b, float c) {
        this.modEnabled = a;
        this.maximumSpeed = b;
        this.minimumSpeed = c;
    }
}
